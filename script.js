function showQuestion() {
  clearInterval(timerInterval);
  timeLeft = 20;
  startTimer();

  const current = filteredQuestions[currentQuestionIndex];
  themeTitle.textContent = `Th√©matique : ${capitalize(current.theme)}`;
  questionEl.textContent = current.question;
  answersEl.innerHTML = "";
  nextBtn.classList.add("hidden");

  current.answers.forEach((answer, index) => {
    const btn = document.createElement("button");
    btn.textContent = answer;
    btn.addEventListener("click", () => selectAnswer(index, current.correct));
    answersEl.appendChild(btn);
  });
}

function selectAnswer(selected, correct) {
  clearInterval(timerInterval);
  const buttons = answersEl.querySelectorAll("button");

  buttons.forEach((btn, index) => {
    btn.disabled = true;
    if (index === correct) {
      btn.classList.add("correct");
    }
    if (index === selected && selected !== correct) {
      btn.classList.add("incorrect");
    }
  });

  if (selected === correct) {
    correctSound.play();
    score++;
  } else {
    wrongSound.play();
  }

  nextBtn.classList.remove("hidden");
}

function showFinalScore() {
  outroAudio.play();
  questionContainer.innerHTML = `<h2>üéâ Quiz termin√© !</h2><p>Score : ${score} / ${filteredQuestions.length}</p>`;
}

function startTimer() {
  timerEl.textContent = `‚è≥ Temps : ${timeLeft}s`;
  timerInterval = setInterval(() => {
    timeLeft--;
    timerEl.textContent = `‚è≥ Temps : ${timeLeft}s`;
    if (timeLeft <= 0) {
      clearInterval(timerInterval);
      selectAnswer(-1, filteredQuestions[currentQuestionIndex].correct);
    }
  }, 1000);
}

function shuffleArray(arr) {
  return [...arr].sort(() => Math.random() - 0.5);
}

